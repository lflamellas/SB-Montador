DOIS_E: EQU 2 
UM: EQU 1
ZERO: EQU 0
SECTION TEXT
IF UM
INPUT OLD_DATA
IF ZERO
LOAD OLD_DATA
L1: DIV DOIS
STORE NEW_DATA
STORE TMP_DATA
LOAD OLD_DATA
SUB TMP_DATA
STORE TMP_DATA
OUTPUT TMP_DATA
COPY NEW_DATA,OLD_DATA
LOAD OLD_DATA
JMPP L1
STORE TMP_DATA
STOP
SECTION DATA
DOIS: CONST DOIS_E
NEG: CONST 2
OLD_DATA: SPACE 3
NEW_DATA: CONST 3
TMP_DATA: SPACE